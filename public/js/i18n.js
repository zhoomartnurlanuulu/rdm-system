// ── Current language ──────────────────────────────────────────────────────────
export let currentLang = localStorage.getItem('rdm-lang') || 'ru';

export function t(key) {
  return (T[currentLang] && T[currentLang][key]) || T.ru[key] || T.en[key] || key;
}

// ── Apply translations to DOM ─────────────────────────────────────────────────
export function applyLang(lang) {
  currentLang = lang;
  localStorage.setItem('rdm-lang', lang);
  document.documentElement.lang = lang === 'ky' ? 'ky' : lang === 'en' ? 'en' : 'ru';

  // Update all data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (el.hasAttribute('data-i18n-html')) {
      el.innerHTML = val;
    } else {
      el.textContent = val;
    }
  });

  // Update placeholders
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    el.placeholder = t(el.getAttribute('data-i18n-ph'));
  });

  // Update lang switcher buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

export function initLang() {
  applyLang(currentLang);
}
